DROP DATABASE car_dealership_db;
CREATE DATABASE car_dealership_db;

use car_dealership_db;

-- Creating tables 
CREATE table vehicles(
	id int auto_increment NOT NULL,
    vin int NOT NULL,
    year_made int,
    make varchar(30),
    model varchar(30),
    color varchar(30),
    vehicle_type varchar(30),
    odometer int,
    price double,
    sold boolean,
    PRIMARY KEY (id)
    );

CREATE table sales_contract(
	sales_contract_id int auto_increment NOT NULL,
    vehicle_id int NOT NULL,
    contract_date datetime NOT NULL,
    customer_name varchar(50) NOT NULL,
    customer_email varchar(50),
    is_financed boolean,
    sales_tax double,
    processing_fee double,
    monthly_payment double,
    total_price double,
    PRIMARY KEY(sales_contract_id)
    );
    
CREATE table lease_contract(
	lease_contract_id int auto_increment NOT NULL,
    vehicle_id int NOT NULL,
    contract_date datetime NOT NULL,
    customer_name varchar(50) NOT NULL,
    customer_email varchar(50),
    expected_ending double,
    lease_fee double,
    monthly_payment double,
    total_price double,
    PRIMARY KEY(lease_contract_id)
    );
    
    
